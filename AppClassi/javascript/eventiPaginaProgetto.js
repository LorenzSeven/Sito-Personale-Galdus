function copiaTesto() 
{
    var testoElement = document.getElementById("testoDaCopiare");
    navigator.clipboard.writeText(testoElement.innerText);

    var pulsanteCopia = document.getElementById("pulsanteCopia");
    pulsanteCopia.style.display = "none";
    var divtestoCopiato = document.getElementById("divTestoCopiato")
    divtestoCopiato.style.display = "block";

    setTimeout(function () 
    {
        pulsanteCopia.style.display = "block";
        divtestoCopiato.style.display = "none";
    }, 3000); // Ripristina dopo 3 secondi

}
